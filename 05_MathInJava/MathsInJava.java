public class MathsInJava {
    public void main(){

        System.out.println("Value of pi: " + Math.PI);
        System.out.println("Value of e: " + Math.E);
        System.out.println("Value of 2^3: " + Math.pow(2, 3));
        System.out.println("Sqrt of 9: " + Math.sqrt(9));
        System.out.println("Absolute value of -56: " + Math.abs(-56));
        System.out.println("Rounded value of 4.553: " + Math.round(4.553));
        System.out.println("Ceil value of 4.6: " + Math.ceil(4.6));
        System.out.println("Max of 4.6: " + Math.max(20, 40));
    }
}
